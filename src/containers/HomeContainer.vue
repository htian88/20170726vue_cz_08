        Node.js

        Node.js 是在浏览器外运行 JS 的主要方式。今年我们看到它发布了许多新版本，提升了性能以及添加对整个 ES6 规范的覆盖。Node 具有快速构建 API、服务器、桌面应用程序甚至机器人的框架，以及创建可以想象到的各种模块的庞大社区。一些你可能想研究的框架：Express, Koa, Next, Nodal。

        PHP

        PHP 首先是一门 Web 语言，并且有大量的 Web 框架可供选择。由于其出色的文档和功能，Laravel 构造了一个积极的社区。Zend Framework 发布了第 3 版，这标志着这个面向业务的框架的巨大升级。Symfony 在今年也发布了很多新的版本，使它作为一个全栈解决方案成为一个更好的选择。

        Ruby

        对于 Ruby，Rails 框架是首选。5.0 版本已于 2016 年发布，为 Web Sockets，API 模式等提供支持。Sinatra 也是小应用程序的不错选择，2.0 版本预计 2017 年发布。

        Python

        Python 有以 Django 和 Flask 的形式组合的全栈/最小框架。Django 1.10 在 8 月发布，为 Postgres 引入了全文搜索和一个大修改的中间件层。

        Java

        Java 生态系统还有流行的 Web 框架可供选择。Play 和 Spark 是两个坚定的选择，作为奖励，它们可以与 Scala 一起使用。

        其他

        对于爱好者，还有 Phoenix 可供选择，这是用 Elixir 语言编写的，并试图成为一个功能完全替代 Rails 的框架而且具有卓越的性能。如果 Elixir 是你想在 2017 年学习的语言之一，不妨尝试 Phoenix。

